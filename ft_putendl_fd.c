#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	while (*s)
	{	
		if (write(fd, s, 1) == -1)
			return ;
		s++;
	}
	write(fd, "\n", 1);
}
